-- 테이블 구조 `bonus`
CREATE TABLE bonus (
  ENAME varchar2(10),
  JOB varchar2(9),
  SAL number,
  COMM number
);

-- 테이블 구조 `dept`
CREATE TABLE dept (
  DEPTNO number NOT NULL,
  DNAME varchar2(14),
  LOC varchar2(13),
  CONSTRAINT PK_DEPT PRIMARY KEY (DEPTNO)
);

-- 테이블 구조 `emp`
CREATE TABLE emp (
  EMPNO number NOT NULL,
  ENAME varchar2(10),
  JOB varchar2(9),
  MGR number,
  HIREDATE date,
  SAL number,
  COMM number,
  DEPTNO number,
  CONSTRAINT PK_EMP PRIMARY KEY (EMPNO),
  CONSTRAINT FK_EMP_DEPT FOREIGN KEY (DEPTNO) REFERENCES dept (DEPTNO) ON DELETE SET NULL
);

-- 테이블 구조 `salgrade`
CREATE TABLE salgrade (
  GRADE number,
  LOSAL number,
  HISAL number
);

